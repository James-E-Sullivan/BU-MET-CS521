'''
Display a pattern
'''

# Assigns each line of the pattern to a variable
line1 = 'FFFFFFF   U     U   NN    NN'
line2 = 'FF        U     U   NNN   NN'
line3 = 'FFFFFFF   U     U   NN N  NN'
line4 = 'FF         U   U    NN  N NN'
line5 = 'FF          UUU     NN   NNN'

# Each line variable is printed, in order
print(line1)
print(line2)
print(line3)
print(line4)
print(line5)
